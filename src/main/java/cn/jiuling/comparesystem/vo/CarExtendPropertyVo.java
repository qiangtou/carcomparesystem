package cn.jiuling.comparesystem.vo;

public class CarExtendPropertyVo {
	
	private String data;//上市时期
	private String weight;//整备质量
	private String displacement; //排量
	private String length;//长度
	private String width;//宽度
	private String height;//高度
	private String trackfront;//前轮距
	private String trackrear;//后轮距
	private String wheelbase;//轴距
	
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getDisplacement() {
		return displacement;
	}
	public void setDisplacement(String displacement) {
		this.displacement = displacement;
	}
	public String getLength() {
		return length;
	}
	public void setLength(String length) {
		this.length = length;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getWheelbase() {
		return wheelbase;
	}
	public void setWheelbase(String wheelbase) {
		this.wheelbase = wheelbase;
	}
	public String getTrackfront() {
		return trackfront;
	}
	public void setTrackfront(String trackfront) {
		this.trackfront = trackfront;
	}
	public String getTrackrear() {
		return trackrear;
	}
	public void setTrackrear(String trackrear) {
		this.trackrear = trackrear;
	}
	
}
